    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Conexion cn = new Conexion();
        
        ResultSet rs;
        
        ConsultaMySQL consulta = new ConsultaMySQL(cn.conectar(), "SELECT * FROM tb_estudiantes");
        
        if (consulta.getError() == null) {
            rs = consulta.getResultado();
            
            try {
                rs.last();
                String nombre = (String)rs.getObject("nombre");
                String apellido1 = rs.getString("apellido1");
                
                jLabel1.setText(nombre + " " + apellido1);
                
            } catch (SQLException ex) {
                Logger.getLogger(FrmConsulta.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

